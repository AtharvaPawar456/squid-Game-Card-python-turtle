import turtle
turtle.bgcolor("black")
t=turtle.Turtle()
t.pensize(4)
turtle.speed(5)
t.color("red")
i = 0
t.penup()
t.fd(150)
t.pendown()
for i in range(4):
    t.fd(100)
    t.lt(90)
t.penup()
t.bk(200)
t.pendown()
t.lt(60)
t.fd(120)
t.rt(120)
t.fd(120)
t.rt(120)
t.fd(120)
t.penup()
t.fd(50)
t.rt(90)
t.fd(50)
t.pendown()
t.circle(50)
turtle.done()